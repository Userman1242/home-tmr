var is_missing = function (x) {
    return isNaN(x) || !isFinite(x) || x === undefined; 
};

var max_array = function(n, lst) {
    var maxval = -Infinity;
    var maxidx = 0;
    var count_miss = 0;
    
    for (var i = 0; i < n; i++) {
        if (is_missing(lst[i])) {
            count_miss++;
        }
        else if (maxval < lst[i]) {
            maxval = lst[i];
            maxidx = i;
        }
    }
    return (count_miss == n) ? -1 : maxidx;
};


